package conexion;

/**
 *
 * @author david
 */
public class Usuarios {
    
    
    String usuario,tipo,estado,nombre,correo;
    int id_user;

    public Usuarios(String usuario, String tipo, String estado, String nombre, String correo, int id_user) {
        this.usuario = usuario;
        this.tipo = tipo;
        this.estado = estado;
        this.nombre = nombre;
        this.correo = correo;
        this.id_user = id_user;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

   

    public Usuarios() {
       
    }
    
    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public int getId_user() {
        return id_user;
    }

    public void setId_user(int id_user) {
        this.id_user = id_user;
    }
    
    
    
}
